/***************************** Include Files *********************************/

#include "xparameters.h"
#include "xttlmembus.h"


/************************** Constant Definitions *****************************/

#define LED 0x01   /* Assumes bit 0 of TTLMEMBUS is connected to an LED  */

/*
 * The following constant maps to the name of the hardware instances that
 * were created in the EDK XPS system.
 */
#define TTLMEMBUS_EXAMPLE_DEVICE_ID  XPAR_LEDS_POSITIONS_DEVICE_ID

/*
 * The following constant is used to wait after an LED is turned on to make
 * sure that it is visible to the human eye.  This constant might need to be
 * tuned for faster or slower processor speeds.
 */
#define LED_DELAY     1000000

/*
 * The following constant is used to determine which channel of the TTLMEMBUS is
 * used for the LED if there are 2 channels supported.
 */
#define LED_CHANNEL 1

/**************************** Type Definitions *******************************/


/***************** Macros (Inline Functions) Definitions *********************/

#ifdef PRE_2_00A_APPLICATION

/*
 * The following macros are provided to allow an application to compile that
 * uses an older version of the driver (pre 2.00a) which did not have a channel
 * parameter. Note that the channel parameter is fixed as channel 1.
 */
#define XTtlMemBus_SetDataDirection(InstancePtr, DirectionMask) \
        XTtlMemBus_SetDataDirection(InstancePtr, LED_CHANNEL, DirectionMask)

#define XTtlMemBus_DiscreteRead(InstancePtr) \
        XTtlMemBus_DiscreteRead(InstancePtr, LED_CHANNEL)

#define XTtlMemBus_DiscreteWrite(InstancePtr, Mask) \
        XTtlMemBus_DiscreteWrite(InstancePtr, LED_CHANNEL, Mask)

#define XTtlMemBus_DiscreteSet(InstancePtr, Mask) \
        XTtlMemBus_DiscreteSet(InstancePtr, LED_CHANNEL, Mask)

#endif

/************************** Function Prototypes ******************************/


/************************** Variable Definitions *****************************/

/*
 * The following are declared globally so they are zeroed and so they are
 * easily accessible from a debugger
 */

XTtlMemBus TtlMemBus; /* The Instance of the TTLMEMBUS Driver */

/*****************************************************************************/
/**
*
* The purpose of this function is to illustrate how to use the TTLMEMBUS level 1
* driver to turn on and off an LED.
*
* @param	None
*
* @return	XST_FAILURE to indicate that the TTLMEMBUS Intialisation had failed.
*
* @note		This function will not return if the test is running.
*
******************************************************************************/
int main(void)
{
	u32 Data;
	int Status;
	volatile int Delay;

	/*
	 * Initialize the TTLMEMBUS driver
	 */
	Status = XTtlMemBus_Initialize(&TtlMemBus, TTLMEMBUS_EXAMPLE_DEVICE_ID);
	if (Status != XST_SUCCESS) {
		return XST_FAILURE;
	}

	/*
	 * Set the direction for all signals to be inputs except the
	 * LED output
	 */
	XTtlMemBus_SetDataDirection(&TtlMemBus, LED_CHANNEL, ~LED);

	/* Loop forever blinking the LED */

	while (1) {
		/*
		 * Read the state of the data so that only the LED state can be
		 * modified
		 */
		Data = XTtlMemBus_DiscreteRead(&TtlMemBus, LED_CHANNEL);

		/*
		 * Set the LED to the opposite state such that it blinks using
		 * the first method, two methods are used for illustration
		 * purposes only
		 */
		if (Data & LED) {
			XTtlMemBus_DiscreteWrite(&TtlMemBus, LED_CHANNEL, Data & ~LED);
		} else {
			XTtlMemBus_DiscreteWrite(&TtlMemBus, LED_CHANNEL, Data | LED);
		}

		/* Wait a small amount of time so the LED is visible */

		for (Delay = 0; Delay < LED_DELAY; Delay++);

		/*
		 * Read the state of the data so that only the LED state can be
		 * modified
		 */
		Data = XTtlMemBus_DiscreteRead(&TtlMemBus, LED_CHANNEL);

		/*
		 * Set the LED to the opposite state such that it blinks using
		 * the other API functions
		 */
		if (Data & LED) {
			XTtlMemBus_DiscreteClear(&TtlMemBus, LED_CHANNEL, LED);
		} else {
			XTtlMemBus_DiscreteSet(&TtlMemBus, LED_CHANNEL, LED);
		}

		/* Wait a small amount of time so the LED is visible */

		for (Delay = 0; Delay < LED_DELAY; Delay++);
	}

	return XST_SUCCESS;
}

